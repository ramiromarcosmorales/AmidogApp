private static final Color FG_ACT    = Color.WHITE;               // blanco puro

public HomeView() {
    initComponents();

    logo.setIcon(FontIcon.of(MaterialDesign.MDI_HOME));

    // Registrar vistas
    contentPanel.add(new HomePanelView(services), "HOME");
    contentPanel.add(new PropietarioView(services), "PROPIETARIOS");
    contentPanel.add(new TurnoView(services), "TURNOS");
    contentPanel.add(new PerroView(services), "PERROS");

    // Cablear navegaci√≥n
    makeNav(lblInicio,       "HOME");
    makeNav(lblPropietarios, "PROPIETARIOS");
    makeNav(lblTurnos,       "TURNOS");
    makeNav(lblPerros,       "PERROS");

    showCard("HOME");
    setActive(lblInicio); // inicio activo al arrancar
}

private void makeNav(javax.swing.JLabel lbl, String card) {
    lbl.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
    lbl.setForeground(FG_INACT); // todos empiezan inactivos (gris)

    lbl.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override public void mouseClicked(java.awt.event.MouseEvent e) {
            setActive(lbl);
            showCard(card);
        }
    });
}

private void setActive(javax.swing.JLabel lbl) {
    // poner el anterior en gris
    if (active != null) active.setForeground(FG_INACT);

    // activar este en blanco
    active = lbl;
    active.setForeground(FG_ACT);
}

private void showCard(String name) {
    ((java.awt.CardLayout) contentPanel.getLayout()).show(contentPanel, name);
}




